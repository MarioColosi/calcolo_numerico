      REAL FUNCTION NORMA1(X,N)
	REAL X(N)
	NORMA1=0
	DO I=1,N
		NORMA1=NORMA1+ABS(X(I))
	END DO
	END

	REAL FUNCTION NORMA2(X,N)
	REAL X(N)
	NORMA2=0
	DO I=1,N
		NORMA2=NORMA2+(X(I)*X(I))
	END DO
	NORMA2=SQRT(NORMA2)
	END

	REAL FUNCTION NORMAINF(X,N)
	REAL X(N)
	NORMAINF=0
	DO I=1,N
		IF(NORMAINF.LT.ABS(X(I)))THEN
			NORMAINF=ABS(X(I))
	    END IF
	END DO
	END
