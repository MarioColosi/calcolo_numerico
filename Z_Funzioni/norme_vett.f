      REAL FUNCTION NORMA1(X,N)
	REAL X(N)
	NORMA1=0
	DO I=1,N
		NORMA1=NORMA1+ABS(X(I))
	END DO
	END

	REAL FUNCTION NORMA2(X,N)
	REAL X(N)
	NORMA2=0
	DO I=1,N
		NORMA2=NORMA2+(X(I)*X(I))
	END DO
	NORMA2=SQRT(NORMA2)
	END

	REAL FUNCTION NORMAINF(X,N)
	REAL X(N)
	NORMA_INF=ABS(X(1))
	DO I=2,N
		IF(NORMA_INF.LT.ABS(X(I)))THEN
			NORMA_INF=ABS(X(I))
	    END IF
	END DO
	END
